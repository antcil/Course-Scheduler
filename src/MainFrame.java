    public void rebuildSemesterComboBoxes()
    {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0)
        {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        }
        else
        {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    public void rebuildCourseComboBoxes() {
        ArrayList<String> courses = CoursesQueries.getAllCourseCodes(currentSemester);
        selectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        dropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        displayCourseListComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
    }
    
    public void rebuildStudentDropCourseComboBox() {
        ArrayList<String> studentCourses = new ArrayList<>();
        String[] name = currentStudent.split(",");
        String lastName = name[0];
        String firstName = name[1];
        String studentID = StudentsQueries.getStudentID(firstName, lastName);
        ArrayList<ScheduleEntry> studentSchedule = ScheduleQueries.getScheduleByStudent(currentSemester, studentID);
        for (ScheduleEntry entry : studentSchedule) {
            studentCourses.add(entry.getCourseCode());
        }
        selectCourseDropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentCourses.toArray()));
    }
    
    public void rebuildStudentComboBoxes() {
        ArrayList<StudentEntry> students = StudentsQueries.getAllStudents();
        selectStudentComboBox1.setModel(new javax.swing.DefaultComboBoxModel());
        selectStudentComboBox2.setModel(new javax.swing.DefaultComboBoxModel());
        dropStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        selectStudentDropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        // Puts proper text in boxes
        for (StudentEntry student : students) {
            selectStudentComboBox1.addItem(student.getNameLayout());
            selectStudentComboBox2.addItem(student.getNameLayout());
            dropStudentComboBox.addItem(student.getNameLayout());
            selectStudentDropCourseComboBox.addItem(student.getNameLayout());
            
        }
    }

