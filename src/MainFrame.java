    private void scheduleCoursesSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCoursesSubmitButtonActionPerformed
        // TODO add your handling code here:
        String courseCode = (String) selectCourseComboBox.getSelectedItem();
        String lastFirst = (String) selectStudentComboBox1.getSelectedItem();
        //Splits given name into seperate names to find studentid with method
        String[] name = lastFirst.split(",");
        String lastName = name[0];
        String firstName = name[1];
        String studentID = StudentsQueries.getStudentID(firstName, lastName);
        String status = "";   
        Timestamp timestamp = new Timestamp(System.currentTimeMillis());
        //Checks available seats and sets appropriate status string
        if (CoursesQueries.getCourseSeats(currentSemester, courseCode) - ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode) > 0) {
             status = "S";
        }
        else {
             status = "W";
        }
        ScheduleEntry schedule = new ScheduleEntry(currentSemester, courseCode, studentID, status, timestamp);
        ScheduleQueries.addScheduleEntry(schedule);
        // Sets appropriate text based on status
        if (status.equals("S")) {
            scheduleCoursesStatusLabel.setText(lastFirst + " has been scheduled for " + courseCode);
        }
        else {
            scheduleCoursesStatusLabel.setText(lastFirst + " has been waitlisted for " + courseCode);
        }
        if (currentStudent != null) {
            rebuildStudentDropCourseComboBox();
        }
    }//GEN-LAST:event_scheduleCoursesSubmitButtonActionPerformed

    private void displayScheduleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayScheduleButtonActionPerformed
        // TODO add your handling code here:
        String lastFirst = (String) selectStudentComboBox2.getSelectedItem();
        String[] name = lastFirst.split(",");
        String lastName = name[0];
        String firstName = name[1];
        String studentid = StudentsQueries.getStudentID(firstName, lastName);
        ArrayList<ScheduleEntry> scheduleList = ScheduleQueries.getScheduleByStudent(currentSemester, studentid);
        DefaultTableModel displayCoursesTabelModel = (DefaultTableModel)displayScheduleTable.getModel();
        
        displayCoursesTabelModel.setNumRows(0);
        Object [] rowData = new Object[2];
        
        for (ScheduleEntry schedule: scheduleList) {
            rowData[0] = schedule.getCourseCode();
            rowData[1] = schedule.getStatus();
            displayCoursesTabelModel.addRow(rowData);
        }
        displayScheduleStatusLabel.setText("Showing schedule of " + lastFirst);
    }//GEN-LAST:event_displayScheduleButtonActionPerformed

