    private void dropStudentSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentSubmitButtonActionPerformed
        // TODO add your handling code here:
        String lastFirst = (String) dropStudentComboBox.getSelectedItem();
        String[] name = lastFirst.split(",");
        String lastName = name[0];
        String firstName = name[1];
        String studentID = StudentsQueries.getStudentID(firstName, lastName);
        StudentsQueries.dropStudent(studentID);
        String changes = "";
        ArrayList<String> semesterList = SemesterQueries.getSemesterList();
        // Traverses target student's schedule in all semesters
        for (String semester : semesterList) {
            changes += "For semester " + semester + ":\n";
            ArrayList<ScheduleEntry> studentSchedule = ScheduleQueries.getScheduleByStudent(semester, studentID);
            // Traverses each schedule entry in each semester
            for (ScheduleEntry schedule : studentSchedule) {
                // If student is removed from schedule course it will schedule the first person on the waitlist
                if (schedule.getStatus().equals("S")) {
                    changes += lastFirst + " " + schedule.getStudentID() + " has been dropped from their scheduled class of " + schedule.getCourseCode() + "\n";
                    ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, schedule.getCourseCode());
                    ArrayList<ScheduleEntry> waitlist = ScheduleQueries.getWaitlistedStudentsByCourse(semester, schedule.getCourseCode());
                    if (!waitlist.isEmpty()) {
                        ScheduleEntry entry = waitlist.get(0);
                        ScheduleQueries.updateScheduleEntry(semester, entry);
                        String newStudentID = entry.getStudentID();
                        StudentEntry student = StudentsQueries.getStudent(newStudentID);
                        changes += student.getNameLayout() + " " + entry.getStudentID() + " has been scheduled for " + entry.getCourseCode() + "\n";
                    }
                }
                else {
                    changes += lastFirst + " " + schedule.getStudentID() + " has been dropped from their waitlisted class of " + schedule.getCourseCode() + "\n";
                    ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, schedule.getCourseCode());
                }
            }
            changes += "\n";
        }
        String allChanges = lastFirst + " " + studentID + " has been removed from the student list\n\n" + changes + lastFirst + " " + studentID + " has been removed from all student combo boxes";
        rebuildStudentComboBoxes();
        dropStudentScheduleChangesTextArea.setText(allChanges);
    }//GEN-LAST:event_dropStudentSubmitButtonActionPerformed

